package com.ibm.bankingapp.responseData;

public class MessageData {
	private String message;
	
	public MessageData() {
		
	}

	public MessageData(String message) {
		super();
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	
}
